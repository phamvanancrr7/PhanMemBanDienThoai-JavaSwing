package ViewModel;

public class QLRam {

    private String ma;
    private String ram;

    public QLRam() {
    }

    public QLRam(String ma, String ram) {
        this.ma = ma;
        this.ram = ram;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getRam() {
        return ram;
    }

    public void setRam(String ram) {
        this.ram = ram;
    }

}
