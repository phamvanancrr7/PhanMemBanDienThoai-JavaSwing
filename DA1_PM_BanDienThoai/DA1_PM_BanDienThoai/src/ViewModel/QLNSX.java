package ViewModel;

public class QLNSX {

    private String ma;
    private String tenNSX;

    public QLNSX() {
    }

    public QLNSX(String ma, String tenNSX) {
        this.ma = ma;
        this.tenNSX = tenNSX;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getTenNSX() {
        return tenNSX;
    }

    public void setTenNSX(String tenNSX) {
        this.tenNSX = tenNSX;
    }

}
