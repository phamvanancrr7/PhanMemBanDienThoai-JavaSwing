package ViewModel;

public class QLCamera {

    private String ma;
    private String cam;

    public QLCamera() {
    }

    public QLCamera(String ma, String cam) {
        this.ma = ma;
        this.cam = cam;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getCam() {
        return cam;
    }

    public void setCam(String cam) {
        this.cam = cam;
    }
    
    
}
