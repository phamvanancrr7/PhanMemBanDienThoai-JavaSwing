package ViewModel;


public class QLHeDieuHanh {
    private String ma;
    private String hdh;

    public QLHeDieuHanh() {
    }

    public QLHeDieuHanh(String ma, String hdh) {
        this.ma = ma;
        this.hdh = hdh;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getHdh() {
        return hdh;
    }

    public void setHdh(String hdh) {
        this.hdh = hdh;
    }
    
    
}
