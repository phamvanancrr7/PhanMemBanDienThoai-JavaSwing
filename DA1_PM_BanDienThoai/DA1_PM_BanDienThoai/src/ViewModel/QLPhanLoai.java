package ViewModel;


public class QLPhanLoai {
    private String maPL;
    private String phanLoai;

    public QLPhanLoai() {
    }

    public QLPhanLoai(String maPL, String phanLoai) {
        this.maPL = maPL;
        this.phanLoai = phanLoai;
    }

    public String getMaPL() {
        return maPL;
    }

    public void setMaPL(String maPL) {
        this.maPL = maPL;
    }

    public String getPhanLoai() {
        return phanLoai;
    }

    public void setPhanLoai(String phanLoai) {
        this.phanLoai = phanLoai;
    }

    
    
}
