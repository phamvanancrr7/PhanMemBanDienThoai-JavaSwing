package ViewModel;

public class QLMauSac {

    private String ma;
    private String mau;

    public QLMauSac() {
    }

    public QLMauSac(String ma, String mau) {
        this.ma = ma;
        this.mau = mau;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getMau() {
        return mau;
    }

    public void setMau(String mau) {
        this.mau = mau;
    }

}
