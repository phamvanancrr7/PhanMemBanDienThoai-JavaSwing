package ViewModel;


public class QLChipSE {
    private String ma;
    private String chip;

    public QLChipSE() {
    }

    public QLChipSE(String ma, String chip) {
        this.ma = ma;
        this.chip = chip;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getChip() {
        return chip;
    }

    public void setChip(String chip) {
        this.chip = chip;
    }
    
    
}
