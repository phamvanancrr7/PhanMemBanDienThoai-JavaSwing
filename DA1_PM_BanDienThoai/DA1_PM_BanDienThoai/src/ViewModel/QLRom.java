package ViewModel;


public class QLRom {
    private String ma;
    private String rom;

    public QLRom() {
    }

    public QLRom(String ma, String rom) {
        this.ma = ma;
        this.rom = rom;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getRom() {
        return rom;
    }

    public void setRom(String rom) {
        this.rom = rom;
    }
    
    
}
